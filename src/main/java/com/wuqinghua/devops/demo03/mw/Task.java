package com.wuqinghua.devops.demo03.mw;

public class Task {
	private int id;
	private String name;
	private Object data; // 执行数据

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Object getData() {
		return data;
	}

	public void setData(Object data) {
		this.data = data;
	}

}
